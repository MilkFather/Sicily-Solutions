bool found;

void search(long v, vector<int> h, int c) {
    if (!found) {
        for (int i = 0; i < 8 && viable[c][i] != -1; i++) {
            if ((v & (1 << viable[c][i])) == 0) {
                if (h.size() == 28) {
                    for (int i = 0; i < 28; i++) printf("%d ", h[i] + 1);
                    printf("%d %d\n", c + 1, viable[c][i] + 1);
                    found = true;
                    return;
                } else {
                    vector<int> nh = h; nh.push_back(c);
                    search(v | (1 << c), nh, viable[c][i]);
                }
            }
        }
    }
}

void work(int s) {
    found = false;
    search(0, vector<int>(), s - 1);
}

